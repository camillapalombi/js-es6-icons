function insertIcons(arrIcons) {
    arrIcons.forEach(element => {
        const elementIcons = document.createElement('div');
        elementIcons.classList.add('icon-box');
        elementIcons.innerHTML = `<i class="${element.family} ${element.prefix}${element.name}" style="color: ${element.color}"></i>
        <div class="text-icon">${element.name}</div>`;
        iconsContainer.append(elementIcons);
    })
};


